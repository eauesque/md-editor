const LOCALE_KEY = "md-editor-locale";
let currentLocale: Locale = "en";

export function initLocale() {
  const saved = localStorage.getItem(LOCALE_KEY) as Locale | null;
  if (saved && translations[saved]) {
    currentLocale = saved;
  } else {
    // Auto-detect from browser
    const lang = navigator.language.toLowerCase();
    if (lang.startsWith("ja")) currentLocale = "ja";
    else if (lang === "zh-tw" || lang === "zh-hant") currentLocale = "zh-tw";
    else if (lang.startsWith("zh")) currentLocale = "zh-cn";
    else if (lang.startsWith("ko")) currentLocale = "ko";
    else currentLocale = "en";
  }
}

export function setLocale(locale: Locale) {
  currentLocale = locale;
  localStorage.setItem(LOCALE_KEY, locale);
}

export function getLocale(): Locale {
  return currentLocale;
}

